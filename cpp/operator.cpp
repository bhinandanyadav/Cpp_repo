#include<iostream>
using namespace std;
int main()
{
    int n;
    cout<<"Input the number:"<<endl;
    cin>>n;
 
    if (10>n)
    {
      cout<<"less than 10"<<endl;
    }
    else if (10<n)
    {
      cout<<"more than 10"<<endl;
    }
    else
    {
      cout<<"equal to 10"<<endl;
    }
    
     
     return 0;
}